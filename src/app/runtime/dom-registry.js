/**
 * DOM element registry. Centralises every querySelector lookup that
 * the legacy main.js performed eagerly at module-load time.
 *
 * createDomRegistry() returns a frozen `els` map with the same shape
 * the legacy code expects. Run it AFTER index.html has parsed.
 *
 * Why a factory: the legacy module-level `const els = { … }` ran the
 * selectors at import time, which made unit-testing painful and tied
 * the file to a live document. The factory keeps the same surface
 * but lets tests swap in a mock root.
 */

function pick(root, selector) {
  return root.querySelector(selector);
}
function pickAll(root, selector) {
  return Array.from(root.querySelectorAll(selector));
}

